package com.experience.model;

public class ExperienceVO implements java.io.Serializable {

	private Integer exper_no;
	private Integer host_no;
	private Integer checker_no;
	private String name;
	private Integer price;
	private String exper_descr;
	private Integer exper_status;
	private Integer exper_type_no;

	public Integer getExper_no() {
		return exper_no;
	}
	public void setExper_no(Integer exper_no) {
		this.exper_no = exper_no;
	}
	public Integer getHost_no() {
		return host_no;
	}
	public void setHost_no(Integer host_no) {
		this.host_no = host_no;
	}
	public Integer getChecker_no() {
		return checker_no;
	}
	public void setChecker_no(Integer checker_no) {
		this.checker_no = checker_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public String getExper_descr() {
		return exper_descr;
	}
	public void setExper_descr(String exper_descr) {
		this.exper_descr = exper_descr;
	}
	public Integer getExper_status() {
		return exper_status;
	}
	public void setExper_status(Integer exper_status) {
		this.exper_status = exper_status;
	}
	public Integer getExper_type_no() {
		return exper_type_no;
	}
	public void setExper_type_no(Integer exper_type_no) {
		this.exper_type_no = exper_type_no;
	}

	

}
